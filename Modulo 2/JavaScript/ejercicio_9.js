//Área y perimetro de un circulo
//Área = 3.1416 * radio elevado al cuadrado
//Perímetro = 2 * 3.1416 * radio


radio_circulo = 12;

Area = 3.1416 * radio_circulo^2;
perimetro = 2 * 3.1416 * radio_circulo;

console.log("El area del circulo es de: " + Area);
console.log("El périmetro del circulo es de: " + perimetro);
